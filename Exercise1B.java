
public class Exercise1B {

	public static void main(String[] args) {
		int xa = 1;
		while (xa < 10) {
			if (xa > 3) {
				System.out.println("big x");
			}
		xa++;
		}
		int xb = 5;
		while (xb > 1) {
			xb--;
			if (xb < 3) {
				System.out.println("small x");
			}
		}
		int xc = 5;
		while (xc > 1) {
			xc--;
			if (xc < 3) {
				System.out.println("small x");
			}
		}
	}

}
